// ─── Main Seed Function ───────────────────────────────────────────────────────

async function main() {
  console.log('Seeding oils...')

  const allOilData = [
    ...CARRIER_OILS.map((o) => ({ ...o, type: 'CARRIER' as const })),
    ...ESSENTIAL_OILS.map((o) => ({ ...o, type: 'ESSENTIAL' as const })),
  ]

  for (const oil of allOilData) {
    await prisma.oil.upsert({
      where: { name: oil.name },
      create: {
        name: oil.name,
        botanicalName: oil.botanicalName,
        type: oil.type,
        origin: oil.origin,
        history: oil.history,
        description: oil.description,
        benefits: oil.benefits,
        contraindications: oil.contraindications,
        aroma: oil.aroma,
        consistency: 'consistency' in oil ? oil.consistency : null,
        absorbency: 'absorbency' in oil ? oil.absorbency : null,
        shelfLifeMonths: 'shelfLifeMonths' in oil ? oil.shelfLifeMonths : null,
        dilutionRateMax: 'dilutionRateMax' in oil ? oil.dilutionRateMax : null,
      },
      update: {
        botanicalName: oil.botanicalName,
        origin: oil.origin,
        history: oil.history,
        description: oil.description,
        benefits: oil.benefits,
        contraindications: oil.contraindications,
        aroma: oil.aroma,
        consistency: 'consistency' in oil ? oil.consistency : null,
        absorbency: 'absorbency' in oil ? oil.absorbency : null,
        shelfLifeMonths: 'shelfLifeMonths' in oil ? oil.shelfLifeMonths : null,
        dilutionRateMax: 'dilutionRateMax' in oil ? oil.dilutionRateMax : null,
      },
    })
  }

  console.log(`  ✓ Upserted ${allOilData.length} oils`)

  const allOils = await prisma.oil.findMany({ select: { id: true, name: true } })
  const byName = new Map(allOils.map((o) => [o.name, o.id]))

  console.log('Seeding notable pairings...')
  let pairingCount = 0

  for (const p of NOTABLE_PAIRINGS) {
    const idA = byName.get(p.a)
    const idB = byName.get(p.b)
    if (!idA || !idB) { console.warn(`  Skipping: ${p.a} or ${p.b} not found`); continue }
    const [sortedA, sortedB] = [idA, idB].sort()
    await prisma.oilPairing.upsert({
      where: { oilAId_oilBId: { oilAId: sortedA, oilBId: sortedB } },
      create: { oilAId: sortedA, oilBId: sortedB, rating: p.rating, reason: p.reason },
      update: { rating: p.rating, reason: p.reason },
    })
    pairingCount++
  }

  console.log('Seeding UNSAFE overrides...')

  for (const p of UNSAFE_PAIRINGS) {
    const idA = byName.get(p.a)
    const idB = byName.get(p.b)
    if (!idA || !idB) { console.warn(`  Skipping UNSAFE: ${p.a} or ${p.b} not found`); continue }
    const [sortedA, sortedB] = [idA, idB].sort()
    await prisma.oilPairing.upsert({
      where: { oilAId_oilBId: { oilAId: sortedA, oilBId: sortedB } },
      create: { oilAId: sortedA, oilBId: sortedB, rating: 'UNSAFE', reason: p.reason },
      update: { rating: 'UNSAFE', reason: p.reason },
    })
    pairingCount++
  }

  console.log(`  ✓ Upserted ${pairingCount} notable pairings`)
  console.log('\nSeed complete.')
  console.log(`  Total oils: ${await prisma.oil.count()}`)
  console.log(`  Total pairings: ${await prisma.oilPairing.count()}`)

  await prisma.$disconnect()
  await pool.end()
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
